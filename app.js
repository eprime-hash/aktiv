async function onReminderToggle() {
  const wantOn = els.reminderEnabled.checked;
  if (wantOn) {
    if (!cloudUser) {
      els.reminderEnabled.checked = false;
      reminderHint("Сначала войди в облако выше.");
      return;
    }
    reminderHint("Запрашиваю разрешение…");
    const ok = await enablePush();
    if (!ok) {
      els.reminderEnabled.checked = false;
      return;
    }
    state.reminders.enabled = true;
    saveState();
  } else {
    state.reminders.enabled = false;
    saveState();
    await disablePush();
  }
  renderReminders();
}

function onReminderPrefsChange() {
  state.reminders.morningTime = els.reminderMorning.value || "09:00";
  state.reminders.eveningTime = els.reminderEvening.value || "21:00";
  state.reminders.eveningEnabled = els.reminderEveningEnabled.checked;
  saveState();
  if (state.reminders.enabled && cloudUser) saveSubscriptionToCloud();
}

function urlBase64ToUint8Array(base64String) {
  const padding = "=".repeat((4 - (base64String.length % 4)) % 4);
  const base64 = (base64String + padding).replace(/-/g, "+").replace(/_/g, "/");
  const raw = atob(base64);
  const out = new Uint8Array(raw.length);
  for (let i = 0; i < raw.length; i += 1) out[i] = raw.charCodeAt(i);
  return out;
}

async function enablePush() {
  try {
    const permission = await Notification.requestPermission();
    if (permission !== "granted") {
      reminderHint("Без разрешения на уведомления напоминания приходить не будут.");
      return false;
    }
    const reg = await navigator.serviceWorker.ready;
    let sub = await reg.pushManager.getSubscription();
    if (!sub) {
      sub = await reg.pushManager.subscribe({
        userVisibleOnly: true,
        applicationServerKey: urlBase64ToUint8Array(vapidPublicKey),
      });
    }
    await saveSubscriptionToCloud(sub);
    reminderHint("Напоминания включены ✓");
    return true;
  } catch {
    reminderHint("Не удалось включить уведомления. На iPhone открой приложение с экрана «Домой» и попробуй снова.");
    return false;
  }
}

async function disablePush() {
  try {
    const reg = await navigator.serviceWorker.ready;
    const sub = await reg.pushManager.getSubscription();
    if (sub) {
      if (supabase && cloudUser) {
        await supabase.from("push_subscriptions").delete().eq("endpoint", sub.endpoint);
      }
      await sub.unsubscribe();
    }
  } catch {
    // тихо: при следующем включении подписка пересоздастся
  }
}

async function saveSubscriptionToCloud(sub) {
  if (!supabase || !cloudUser) return;
  try {
    if (!sub) {
      const reg = await navigator.serviceWorker.ready;
      sub = await reg.pushManager.getSubscription();
    }
    if (!sub) return;
    const r = state.reminders || {};
    await supabase.from("push_subscriptions").upsert(
      {
        user_id: cloudUser.id,
        endpoint: sub.endpoint,
        subscription: sub.toJSON(),
        tz: Intl.DateTimeFormat().resolvedOptions().timeZone || "UTC",
        morning_time: r.morningTime || "09:00",
        evening_time: r.eveningEnabled === false ? null : r.eveningTime || "21:00",
        enabled: Boolean(r.enabled),
        updated_at: new Date().toISOString(),
      },
      { onConflict: "endpoint" }
    );
  } catch {
    // данные напоминаний синхронизируются и в общем состоянии; повторим позже
  }
}


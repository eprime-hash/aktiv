function getStreakDays() {
  const protectedSet = new Set(state.streak?.protectedDates || []);
  let streak = 0;
  const cursor = new Date();

  // If today has no coins yet, start counting from yesterday so a morning
  // session doesn't wipe an earned streak.
  if (getDayCoins(formatDate(cursor)) <= 0 && !protectedSet.has(formatDate(cursor))) {
    cursor.setDate(cursor.getDate() - 1);
  }

  while (true) {
    const date = formatDate(cursor);
    if (getDayCoins(date) > 0) {
      streak += 1; // real active day
    } else if (protectedSet.has(date)) {
      // a shield bridges this missed day without breaking the chain
    } else {
      break;
    }
    cursor.setDate(cursor.getDate() - 1);
  }

  return streak;
}

function dateNDaysAgo(n) {
  const date = new Date();
  date.setDate(date.getDate() - n);
  return formatDate(date);
}

// Keeps the streak resilient: spends a shield to bridge a single missed day,
// tracks the personal record, and awards a shield at each 7-day milestone.
function reconcileStreak() {
  state.streak ||= { best: 0, shields: 0, protectedDates: [], rewardedMilestones: [] };
  const s = state.streak;
  const protectedSet = new Set(s.protectedDates || []);
  const isActive = (date) => getDayCoins(date) > 0 || protectedSet.has(date);

  // Auto-protect an isolated missed yesterday if a shield is banked.
  const yesterday = dateNDaysAgo(1);
  const dayBefore = dateNDaysAgo(2);
  if (!isActive(yesterday) && isActive(dayBefore) && (s.shields || 0) > 0) {
    protectedSet.add(yesterday);
    s.protectedDates = [...protectedSet];
    s.shields -= 1;
    // Будущий Я подстраховал тебя щитом — теперь ты в долгу перед собой.
    state.bond ||= { trust: 0, debt: 0, lastTick: "", peak: 0 };
    state.bond.debt = (state.bond.debt || 0) + 1;
  }

  const current = getStreakDays();
  s.best = Math.max(s.best || 0, current);

  s.rewardedMilestones ||= [];
  for (let milestone = 7; milestone <= current; milestone += 7) {
    if (!s.rewardedMilestones.includes(milestone)) {
      s.rewardedMilestones.push(milestone);
      if ((s.shields || 0) < 3) s.shields = (s.shields || 0) + 1;
    }
  }
}

// --- Связь с Будущим Я (доверие, долг, отношения) ------------------------

const BOND_TIERS = [
  { min: 0, name: "Знакомство", note: "Будущий ты ещё присматривается к тебе." },
  { min: 25, name: "Доверие", note: "Он начал верить, что ты дойдёшь." },
  { min: 50, name: "Близость", note: "Вы заодно. Он говорит с тобой открыто." },
  { min: 75, name: "Союз", note: "Он на тебя полагается. Вы строите его вместе." },
  { min: 95, name: "Единство", note: "Вы одно целое. Грань между вами стирается." },
];

function getBondTier(trust) {
  let t = BOND_TIERS[0];
  for (const tier of BOND_TIERS) if (trust >= tier.min) t = tier;
  return t;
}

// Доверие растёт за активные дни и мягко тает в простое (асимметрично — прощающе).
// Долг гасится активными днями. Идемпотентно по дате, без начисления задним числом.
function reconcileBond() {
  state.bond ||= { trust: 0, debt: 0, lastTick: "", peak: 0 };
  const b = state.bond;
  const protectedSet = new Set(state.streak?.protectedDates || []);
  if (!b.lastTick) {
    // Первый запуск: оцениваем доверие по текущей серии, чтобы не начинать с нуля.
    b.trust = Math.min(100, getStreakDays() * 4);
    b.peak = b.trust;
    b.lastTick = dateNDaysAgo(1);
    return;
  }
  const cursor = new Date(`${b.lastTick}T12:00:00`);
  cursor.setDate(cursor.getDate() + 1);
  const yesterday = new Date(`${todayIso()}T12:00:00`);
  yesterday.setDate(yesterday.getDate() - 1);
  let guard = 0;
  while (cursor <= yesterday && guard < 120) {
    guard += 1;
    const d = formatDate(cursor);
    if (getDayCoins(d) > 0) {
      b.trust = Math.min(100, b.trust + 4);
      if (b.debt > 0) b.debt -= 1; // вернул долг делом
    } else if (!protectedSet.has(d)) {
      b.trust = Math.max(0, b.trust - 3);
    }
    cursor.setDate(cursor.getDate() + 1);
  }
  // Сегодняшняя активность гасит долг сразу (приятная мгновенная отдача).
  if (b.debt > 0 && getDayCoins(todayIso()) > 0) b.debt -= 1;
  b.peak = Math.max(b.peak || 0, b.trust);
  b.lastTick = formatDate(yesterday);
}


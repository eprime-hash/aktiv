// Ask the browser to keep our localStorage from being auto-evicted under disk
// pressure. Installed PWAs are usually granted this silently.
async function requestPersistentStorage() {
  try {
    if (navigator.storage?.persisted && !(await navigator.storage.persisted())) {
      await navigator.storage.persist();
    }
  } catch {
    // Not supported — localStorage still persists normally, just without the guarantee.
  }
}


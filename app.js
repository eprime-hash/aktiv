async function submitAuth() {
  const email = (document.getElementById("authEmailInput")?.value || "").trim();
  const password = document.getElementById("authPasswordInput")?.value || "";
  const message = document.getElementById("authMessage");
  if (!email || !email.includes("@")) {
    if (message) message.textContent = "Введи корректный email.";
    return;
  }
  if (password.length < 6) {
    if (message) message.textContent = "Пароль — минимум 6 символов.";
    return;
  }
  if (message) message.textContent = authMode === "login" ? "Вхожу…" : "Создаю аккаунт…";

  try {
    if (authMode === "login") {
      const { error } = await supabase.auth.signInWithPassword({ email, password });
      if (error && message) {
        message.textContent = "Неверный email или пароль. Если аккаунта ещё нет — нажми «Создать».";
      }
      // успех обрабатывает onAuthStateChange (подтянет данные и закроет окно)
    } else {
      const { data, error } = await supabase.auth.signUp({ email, password });
      if (error) {
        if (message) {
          message.textContent = /registered|already/i.test(error.message)
            ? "Этот email уже зарегистрирован — переключись на «Войти»."
            : "Не удалось создать аккаунт. Попробуй другой email или пароль.";
        }
        return;
      }
      if (!data.session && message) {
        message.textContent = "Аккаунт создан. Подтверди email по ссылке из письма, затем войди.";
      }
    }
  } catch {
    if (message) message.textContent = "Облако недоступно. Проверь интернет и попробуй ещё раз.";
  }
}


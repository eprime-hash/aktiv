function finishOnboarding(skipped) {
  const type = archetypes[onboardingDraft.archetype] || archetypes.balanced;
  const selected = skipped ? new Set(type.starters) : new Set(onboardingDraft.habitIds);
  const defaultIds = defaultHabits.map((habit) => habit.id);

  state.disabledDefaultHabitIds = defaultIds.filter((id) => !selected.has(id));

  const existingTitles = new Set(getHabits().map((habit) => habit.title.toLowerCase()));
  selected.forEach((id) => {
    if (defaultIds.includes(id)) return;
    const def = habitCatalog.find((habit) => habit.id === id);
    if (!def || existingTitles.has(def.title.toLowerCase())) return;
    state.customHabits.push({
      id: `starter-${def.id}-${Date.now()}-${Math.floor(Math.random() * 1000)}`,
      title: def.title,
      sector: def.sector,
      coins: def.coins,
      benchmark: def.benchmark,
      why: def.why,
      custom: true,
      fromLibrary: true,
    });
    existingTitles.add(def.title.toLowerCase());
  });

  state.profile = {
    onboardedAt: new Date().toISOString(),
    archetype: onboardingDraft.archetype || "balanced",
    name: state.profile?.name || "",
    bigGoal: onboardingDraft.bigGoal || "",
    goalTargetAssets: onboardingDraft.goalTarget || 0,
  };

  if (onboardingDraft.bigGoal) {
    state.weeklyPlan = { ...getCurrentWeeklyPlan(), focus: state.weeklyPlan.focus || onboardingDraft.bigGoal };
  }

  saveState();
  closeOnboarding();
  render();
  if (!prefersReducedMotion()) burstConfetti(80);
  showRewardToast(type.icon, "Терминал настроен", "Твой актив активирован. Закрой первое действие сегодня.");
}


// --- Дивиденды дисциплины (компаунд за постоянство) ----------------------

function getDividendRate(streak) {
  let rate = DIVIDEND_BASE_RATE;
  if (streak >= 3) rate += 0.0005;
  if (streak >= 7) rate += 0.0005;
  if (streak >= 30) rate += 0.0005;
  return rate; // 0.05% .. 0.2% в день
}

function cumulativeCoinsThrough(dateIso) {
  let coins = 0;
  for (const d in state.completions || {}) {
    if (d <= dateIso) {
      coins += Object.values(state.completions[d]).reduce((sum, e) => sum + Number(e.coins || 0), 0);
    }
  }
  return coins;
}

function streakEndingAt(dateIso) {
  const protectedSet = new Set(state.streak?.protectedDates || []);
  let streak = 0;
  const cursor = new Date(`${dateIso}T12:00:00`);
  while (true) {
    const d = formatDate(cursor);
    if (getDayCoins(d) > 0 || protectedSet.has(d)) streak += 1;
    else break;
    cursor.setDate(cursor.getDate() - 1);
  }
  return streak;
}

// Начисляет дивиденды за прошедшие активные дни (идемпотентно по дате).
// Только активные дни приносят доход; база компаундится (потенциал + баланс).
function accrueDividends() {
  const div = state.dividends;
  const today = todayIso();
  if (!div.lastAccrual) {
    // Первый запуск — начинаем со «вчера», не начисляем задним числом.
    div.lastAccrual = dateNDaysAgo(1);
    return 0;
  }
  let credited = 0;
  const cursor = new Date(`${div.lastAccrual}T12:00:00`);
  cursor.setDate(cursor.getDate() + 1); // первый необработанный день
  const yesterday = new Date(`${today}T12:00:00`);
  yesterday.setDate(yesterday.getDate() - 1);
  let guard = 0;
  while (cursor <= yesterday && guard < DIVIDEND_MAX_LOOKBACK) {
    guard += 1;
    const d = formatDate(cursor);
    if (getDayCoins(d) > 0) {
      const base = cumulativeCoinsThrough(d) * COIN_VALUE + div.balance;
      const amount = base * getDividendRate(streakEndingAt(d));
      div.balance += amount;
      div.lifetimeEarned += amount;
      credited += amount;
      div.history.unshift({ date: d, amount: Math.round(amount * 100) / 100 });
      div.history = div.history.slice(0, 30);
    }
    cursor.setDate(cursor.getDate() + 1);
  }
  div.lastAccrual = formatDate(yesterday);
  return credited;
}


function renderRadar() {
  const data = sectorCoinsWindow(30);
  const keys = Object.keys(sectors);
  const max = Math.max(1, ...keys.map((key) => data[key] || 0));
  const size = 240;
  const cx = size / 2;
  const cy = size / 2;
  const r = size / 2 - 34;
  const n = keys.length;

  const angle = (i) => ((-90 + (i * 360) / n) * Math.PI) / 180;
  const pointAt = (i, ratio) => [cx + Math.cos(angle(i)) * r * ratio, cy + Math.sin(angle(i)) * r * ratio];

  const rings = [0.25, 0.5, 0.75, 1]
    .map((ratio) => {
      const pts = keys.map((_, i) => pointAt(i, ratio).map((value) => value.toFixed(1)).join(",")).join(" ");
      return `<polygon class="radar-ring" points="${pts}" />`;
    })
    .join("");

  const axes = keys
    .map((_, i) => {
      const [x, y] = pointAt(i, 1);
      return `<line class="radar-axis" x1="${cx}" y1="${cy}" x2="${x.toFixed(1)}" y2="${y.toFixed(1)}" />`;
    })
    .join("");

  const shapePts = keys.map((key, i) => pointAt(i, (data[key] || 0) / max).map((value) => value.toFixed(1)).join(",")).join(" ");

  const labels = keys
    .map((key, i) => {
      const [x, y] = pointAt(i, 1.18);
      return `<text class="radar-label" x="${x.toFixed(1)}" y="${y.toFixed(1)}" text-anchor="middle" dominant-baseline="middle">${sectors[key].label}</text>`;
    })
    .join("");

  els.sectorRadar.innerHTML = `
    <svg viewBox="0 0 ${size} ${size}" role="img" aria-label="Радар капитала по секторам">
      ${rings}
      ${axes}
      <polygon class="radar-shape" points="${shapePts}" />
      ${labels}
    </svg>
  `;
}


// --- Реальность: виртуальное против реального ------------------------------

function monthKeyOf(date) {
  return `${date.getFullYear()}-${String(date.getMonth() + 1).padStart(2, "0")}`;
}

function monthLabel(monthKey) {
  const [y, m] = monthKey.split("-").map(Number);
  const label = new Date(y, m - 1, 1).toLocaleDateString("ru-RU", { month: "long", year: "numeric" });
  return label.charAt(0).toUpperCase() + label.slice(1).replace(" г.", "");
}

function getLatestReal() {
  const log = state.realLog || [];
  if (!log.length) return null;
  return [...log].sort((a, b) => b.monthKey.localeCompare(a.monthKey))[0];
}

function getMonthlyVirtual(monthKey) {
  let coins = 0;
  Object.entries(state.completions || {}).forEach(([date, day]) => {
    if (date.startsWith(monthKey)) {
      coins += Object.values(day).reduce((sum, e) => sum + Number(e.coins || 0), 0);
    }
  });
  return coins * COIN_VALUE;
}

// Капитализация: какая доля созданного за месяц виртуального вклада обернулась
// реальным доходом. >100% = рычаг (денег больше, чем «вложено»). null = нет данных.
function getCapitalizationPct() {
  const latest = getLatestReal();
  if (!latest) return null;
  const virtual = getMonthlyVirtual(latest.monthKey);
  if (virtual <= 0) return null;
  return Math.round((latest.income / virtual) * 100);
}

function saveRealMonth() {
  const monthKey = els.realMonthSelect.value;
  const income = Math.max(0, Math.round(Number(els.realIncomeInput.value || 0)));
  const savings = Math.max(0, Math.round(Number(els.realSavingsInput.value || 0)));
  const note = els.realNoteInput.value.trim();
  if (!monthKey || (!income && !savings)) return;

  const prevLatest = getLatestReal();
  state.realLog = (state.realLog || []).filter((e) => e.monthKey !== monthKey);
  state.realLog.push({ id: `real-${monthKey}`, monthKey, income, savings, note, createdAt: new Date().toISOString() });
  state.realLog.sort((a, b) => a.monthKey.localeCompare(b.monthKey));
  els.realIncomeInput.value = "";
  els.realSavingsInput.value = "";
  els.realNoteInput.value = "";
  saveState();
  render();
  // Празднуем рост реального дохода — это и есть главная цель всей системы.
  if (prevLatest && prevLatest.monthKey !== monthKey && income > prevLatest.income) {
    showRewardToast("💰", "Реальный доход вырос", `${money(prevLatest.income)} → ${money(income)}. Дисциплина монетизируется.`);
    if (!prefersReducedMotion()) burstConfetti(80);
  }
}


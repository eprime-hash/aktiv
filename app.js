function renderHabits() {
  const habits = getHabits().filter((habit) => activeFilter === "all" || habit.sector === activeFilter);
  els.habitList.innerHTML = "";

  if (!habits.length) {
    els.habitList.innerHTML = `<p class="empty-state">В этом секторе пока нет привычек. Добавь из библиотеки ниже.</p>`;
    return;
  }

  // Раскладываем по ритму: что актуально сегодня — наверх, отдыхающее — в тихую
  // сворачиваемую группу, чтобы периодические привычки не давили каждый день.
  const active = [];
  const resting = [];
  habits.forEach((habit) => {
    const status = getHabitStatus(habit);
    (status.state === "resting" ? resting : active).push({ habit, status });
  });

  // Сначала то, что ждёт действия (перезревшее выше), затем уже сделанное.
  active.sort((a, b) => {
    const rank = (s) => (s.state === "done" ? 1 : 0);
    if (rank(a.status) !== rank(b.status)) return rank(a.status) - rank(b.status);
    return (b.status.overdueBy || 0) - (a.status.overdueBy || 0);
  });

  active.forEach(({ habit, status }) => els.habitList.appendChild(buildHabitItem(habit, status)));

  if (resting.length) {
    resting.sort((a, b) => (a.status.inDays || 0) - (b.status.inDays || 0));
    const group = document.createElement("details");
    group.className = "resting-group";
    const summary = document.createElement("summary");
    summary.innerHTML = `<span>😴 Отдыхают по плану · ${resting.length}</span><span class="resting-hint">вернутся, когда подойдёт срок</span>`;
    group.appendChild(summary);
    resting.forEach(({ habit, status }) => group.appendChild(buildHabitItem(habit, status)));
    els.habitList.appendChild(group);
  }
}


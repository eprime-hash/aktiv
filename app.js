function syncChronicle() {
  const c = state.chronicle;
  const fired = new Set(c.firedKeys);
  const level = getLevel();
  const streak = getStreakDays();
  const today = todayIso();
  const goal = state.profile?.bigGoal;
  const arch = archetypes[state.profile?.archetype]?.label || "твой путь";

  if (!c.initialized) {
    c.initialized = true;
    c.lastLevel = level;
    c.lastMonth = monthKeyOf(new Date());
    getHabits().forEach((h) => (state.habitStreaks?.[h.id]?.celebrated || []).forEach((m) => fired.add(`record-${h.id}-${m}`)));
    [30, 100, 365].forEach((n) => {
      if (streak >= n) fired.add(`streak-${n}`);
    });
    if (state.profile?.onboardedAt) {
      addChapter(c, fired, {
        key: "origin",
        title: "Глава 1. Решение",
        text: `Здесь начинается история о том, как ты решил стать тем, кто доводит до конца.${goal ? ` Большая цель названа: ${goal}.` : ""} Путь — «${arch}». С этого дня каждый твой шаг становится строкой этой летописи.`,
        prompt: `я только что начал свой путь${goal ? ` с целью «${goal}»` : ""} как «${arch}» — это самая первая глава, глава решения`,
      });
    }
    c.firedKeys = [...fired];
    return;
  }

  if (level > (c.lastLevel || 1)) {
    for (let L = (c.lastLevel || 1) + 1; L <= level; L += 1) {
      if (LEVEL_CHAPTERS.includes(L)) {
        addChapter(c, fired, {
          key: `level-${L}`,
          title: `Уровень ${L}`,
          text: `Ты перешёл на уровень ${L}. То, что когда-то требовало усилия, стало частью тебя. Актив растёт — и ты вместе с ним.`,
          prompt: `я достиг уровня ${L} — заметная веха роста`,
        });
      }
    }
    c.lastLevel = level;
  }

  getHabits().forEach((h) => {
    (state.habitStreaks?.[h.id]?.celebrated || []).forEach((m) => {
      if (m >= 30) {
        addChapter(c, fired, {
          key: `record-${h.id}-${m}`,
          title: `${h.title}: ${m} подряд`,
          text: `«${h.title}» — ${formatDays(m)} без обрыва. Это уже не попытка, а характер.`,
          prompt: `у меня рекорд: привычку «${h.title}» я держу ${m} раз подряд`,
        });
      }
    });
  });

  [30, 100, 365].forEach((n) => {
    if (streak >= n) {
      addChapter(c, fired, {
        key: `streak-${n}`,
        title: `Серия ${n}`,
        text: `${formatDays(n)} подряд. Большинство сходит с дистанции куда раньше. Ты — нет.`,
        prompt: `моя общая серия достигла ${n} дней подряд`,
      });
    }
  });

  if (getDayCoins(today) > 0) {
    let run = 0;
    const cur = new Date();
    while (getDayCoins(formatDate(cur)) > 0) {
      run += 1;
      cur.setDate(cur.getDate() - 1);
    }
    let gap = 0;
    while (getDayCoins(formatDate(cur)) <= 0 && gap < 90) {
      gap += 1;
      cur.setDate(cur.getDate() - 1);
    }
    const hadBefore = getDayCoins(formatDate(cur)) > 0;
    if (run <= 2 && gap >= 2 && hadBefore) {
      const runStart = formatDate(new Date(Date.now() - (run - 1) * 86400000));
      addChapter(c, fired, {
        key: `comeback-${runStart}`,
        title: "Возвращение",
        text: `Был перерыв в ${formatDays(gap)} — и ты вернулся. Именно это отличает тех, кто доходит: не отсутствие падений, а возвращение после них.`,
        prompt: `я вернулся после паузы в ${gap} дн. — снова в строю`,
      });
    }
  }

  const curMonth = monthKeyOf(new Date());
  if (c.lastMonth && curMonth !== c.lastMonth) {
    const v = getMonthlyVirtual(c.lastMonth);
    if (v > 0) {
      addChapter(c, fired, {
        key: `month-${c.lastMonth}`,
        title: `Итог месяца · ${monthLabel(c.lastMonth)}`,
        text: `Месяц ${monthLabel(c.lastMonth)} закрыт: создано ${money(v)} потенциала. Месяцы складываются в годы, а годы — в другого человека.`,
        prompt: `я закрыл месяц ${monthLabel(c.lastMonth)}, создав ${money(v)} потенциала`,
      });
    }
    c.lastMonth = curMonth;
  }

  c.firedKeys = [...fired];
}

function chapterWord(value) {
  const m10 = value % 10;
  const m100 = value % 100;
  if (m10 === 1 && m100 !== 11) return "глава";
  if (m10 >= 2 && m10 <= 4 && (m100 < 12 || m100 > 14)) return "главы";
  return "глав";
}

function chronicleNextHint() {
  const toNext = LEVEL_SIZE - (getTotalCoins() % LEVEL_SIZE);
  return `Следующая глава пишется твоими действиями — например, ещё ${toNext} ${coinWord(toNext)} до нового уровня.`;
}

function shareChronicleChapter(id) {
  const ch = state.chronicle?.chapters.find((c) => c.id === id);
  if (!ch) return;
  const text = `📖 ${ch.title}\n\n${ch.text}\n\n— из моей хроники становления в «Актив Я»`;
  if (typeof navigator.share === "function") navigator.share({ text }).catch(() => {});
  else {
    navigator.clipboard?.writeText(text);
    window.alert("Глава скопирована — можно вставить куда угодно.");
  }
}

function renderChronicle() {
  if (!els.chronicleBody) return;
  const asc = [...(state.chronicle?.chapters || [])].sort((a, b) => String(a.date).localeCompare(String(b.date)));
  asc.forEach((ch, i) => (ch._num = i + 1));
  const total = asc.length;
  const hint = chronicleNextHint();
  if (!total) {
    els.chronicleBody.innerHTML = `<p class="chronicle-intro">Здесь будущий ты ведёт летопись твоего становления — главы появляются за реальные вехи: первый шаг, уровни, рекорды, возвращения после паузы. ${hint}</p>`;
    return;
  }
  const desc = [...asc].reverse();
  els.chronicleBody.innerHTML = `
    <p class="chronicle-intro">${total} ${chapterWord(total)} твоей истории. ${hint}</p>
    <div class="chronicle-list">
      ${desc
        .map((ch) => {
          const d = new Date(ch.date).toLocaleDateString("ru-RU", { day: "numeric", month: "short", year: "numeric" });
          return `
          <article class="chronicle-chapter">
            <div class="chronicle-meta"><span>Глава ${ch._num}${ch.aiWritten ? " · ✨" : ""}</span><span>${d}</span></div>
            <h3>${escapeHtml(ch.title)}</h3>
            <p>${escapeHtml(ch.text)}</p>
            <button type="button" class="chronicle-share" data-share-chapter="${ch.id}">Поделиться главой</button>
          </article>`;
        })
        .join("")}
    </div>`;
  els.chronicleBody.querySelectorAll("[data-share-chapter]").forEach((b) => {
    b.addEventListener("click", () => shareChronicleChapter(b.dataset.shareChapter));
  });
}

function syncFutureSelf() {
  const fs = state.futureSelf;
  const today = todayIso();
  const streak = getStreakDays();
  const level = getLevel();
  const todayCoins = getDayCoins(today);
  const goal = state.profile?.bigGoal;

  const add = (type, text) => {
    fs.messages.unshift({ id: `fs-${Date.now()}-${Math.random().toString(36).slice(2, 6)}`, date: new Date().toISOString(), type, text, from: "future" });
    fs.messages = fs.messages.slice(0, 80);
  };
  const fire = (key, type, text) => {
    if (!fs.firedKeys.includes(key)) {
      fs.firedKeys.push(key);
      add(type, text);
    }
  };

  if (fs.lastDaily !== today) {
    fs.lastDaily = today;
    const text = todayCoins > 0 ? pickMsg(fsDone()) : streak > 0 ? pickMsg(fsRisk(streak)) : pickMsg(fsFresh(goal));
    add("daily", text);
  }
  STREAK_MILESTONES.forEach((m) => {
    if (streak >= m) fire(`streak-${m}`, "milestone", pickMsg(fsMilestone(m)));
  });
  if (level > (fs.lastLevel || 1)) {
    fire(`level-${level}`, "level", pickMsg(fsLevel(level, getRank(level))));
  }
  fs.lastLevel = Math.max(fs.lastLevel || 1, level);
  if (todayCoins >= DAILY_TARGET) fire(`target-${today}`, "target", pickMsg(fsTarget()));

  // Связь: будущий ты сам реагирует на изменения доверия и долга.
  const b = state.bond;
  if (b) {
    const tierIdx = BOND_TIERS.indexOf(getBondTier(b.trust || 0));
    if (b.lastTier == null) {
      b.lastTier = tierIdx; // первый запуск — без сообщения
    } else if (tierIdx > b.lastTier) {
      add("bond", pickMsg(fsBondUp(getBondTier(b.trust || 0).name)));
      b.lastTier = tierIdx;
    } else if (tierIdx < b.lastTier) {
      add("bond", pickMsg(fsBondDown()));
      b.lastTier = tierIdx;
    }
    const prevDebt = b.lastDebt == null ? b.debt || 0 : b.lastDebt;
    if ((b.debt || 0) > prevDebt) add("bond", pickMsg(fsDebtIncurred()));
    else if ((b.debt || 0) === 0 && prevDebt > 0) add("bond", pickMsg(fsDebtCleared()));
    b.lastDebt = b.debt || 0;
  }
}

function fsRelativeDate(iso) {
  const then = new Date(iso);
  const days = Math.floor((new Date(`${todayIso()}T00:00:00`) - new Date(`${formatDate(then)}T00:00:00`)) / 86400000);
  if (days <= 0) return "сегодня";
  if (days === 1) return "вчера";
  return `${formatDays(days)} назад`;
}

function fsUnseenCount() {
  const lastSeen = state.futureSelf?.lastSeen || "";
  return (state.futureSelf?.messages || []).filter((m) => m.date > lastSeen).length;
}

function markFutureSeen() {
  if (!state.futureSelf) return;
  state.futureSelf.lastSeen = new Date().toISOString();
  saveState();
  renderFutureTeaser();
}

function renderFutureTeaser() {
  if (!els.futureTeaser) return;
  const latest = state.futureSelf?.messages?.[0];
  if (!latest) {
    els.futureTeaser.hidden = true;
    return;
  }
  els.futureTeaser.hidden = false;
  const unseen = fsUnseenCount();
  els.futureTeaserText.textContent = latest.text;
  els.futureTeaserBadge.hidden = unseen === 0;
  els.futureTeaserBadge.textContent = unseen > 0 ? `${unseen} новое` : "";
  if (els.futureTeaserBond) {
    const b = state.bond || {};
    const tier = getBondTier(b.trust || 0);
    els.futureTeaserBond.textContent = b.debt > 0 ? `· долг ${b.debt}` : `· ${tier.name} ${Math.round(b.trust || 0)}%`;
    els.futureTeaserBond.classList.toggle("teaser-bond-debt", b.debt > 0);
  }
}

let fsTyping = false;
let fsSending = false;

function renderFutureFeed() {
  if (!els.futureFeed) return;
  const messages = (state.futureSelf?.messages || []).slice(0, 40).reverse(); // в хронологическом порядке

  let html = "";
  if (!messages.length && !fsTyping) {
    html = `<p class="insight">Это ты будущий — тот, кем ты становишься. Напиши ему, или просто сделай шаг сегодня, и он откликнется.</p>`;
  } else {
    html = messages
      .map((m) => {
        if (m.from === "you") {
          return `<article class="fs-message you"><div class="fs-bubble you"><p>${escapeHtml(m.text)}</p></div></article>`;
        }
        return `<article class="fs-message"><span class="fs-avatar" aria-hidden="true">🧬</span><div class="fs-bubble"><p>${escapeHtml(m.text)}</p><time>${fsRelativeDate(m.date)}</time></div></article>`;
      })
      .join("");
    if (fsTyping) {
      html += `<article class="fs-message"><span class="fs-avatar" aria-hidden="true">🧬</span><div class="fs-bubble fs-typing"><span></span><span></span><span></span></div></article>`;
    }
  }
  els.futureFeed.innerHTML = html;
  els.futureFeed.scrollTop = els.futureFeed.scrollHeight;
}


function buildWeekBuckets(count) {
  const out = [];
  const now = new Date();
  for (let w = 0; w < count; w += 1) {
    const monday = new Date(now);
    monday.setDate(now.getDate() - ((now.getDay() + 6) % 7) - w * 7);
    monday.setHours(12, 0, 0, 0);
    const sunday = new Date(monday);
    sunday.setDate(monday.getDate() + 6);
    let coins = 0;
    let active = 0;
    let maxDays = 0;
    for (let i = 0; i < 7; i += 1) {
      const d = new Date(monday);
      d.setDate(monday.getDate() + i);
      if (d > now) continue;
      maxDays += 1;
      const c = getDayCoins(formatDate(d));
      coins += c;
      if (c > 0) active += 1;
    }
    const range = `${monday.toLocaleDateString("ru-RU", { day: "numeric", month: "short" })} – ${sunday.toLocaleDateString("ru-RU", { day: "numeric", month: "short" })}`;
    out.push({ label: w === 0 ? `Эта неделя · ${range}` : range, coins, activeDays: active, maxDays });
  }
  return out;
}

function buildMonthBuckets(count) {
  const out = [];
  const now = new Date();
  for (let m = 0; m < count; m += 1) {
    const month = new Date(now.getFullYear(), now.getMonth() - m, 1);
    const daysInMonth = new Date(month.getFullYear(), month.getMonth() + 1, 0).getDate();
    let coins = 0;
    let active = 0;
    let maxDays = 0;
    for (let day = 1; day <= daysInMonth; day += 1) {
      const d = new Date(month.getFullYear(), month.getMonth(), day);
      if (d > now) continue;
      maxDays += 1;
      const c = getDayCoins(formatDate(d));
      coins += c;
      if (c > 0) active += 1;
    }
    const label = month.toLocaleDateString("ru-RU", { month: "long", year: "numeric" });
    out.push({ label: label.charAt(0).toUpperCase() + label.slice(1), coins, activeDays: active, maxDays });
  }
  return out;
}


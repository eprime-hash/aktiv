let ritualOrderIds = null;

function ensureRitualOverlay() {
  let overlay = document.getElementById("ritual");
  if (!overlay) {
    overlay = document.createElement("div");
    overlay.id = "ritual";
    overlay.className = "ritual";
    document.body.appendChild(overlay);
  }
  overlay.hidden = false;
  document.body.classList.add("ritual-open");
  return overlay;
}

function openRitual() {
  const today = todayIso();
  const completions = state.completions[today] || {};
  const habits = getHabits();
  // Undone first for fast tapping; order frozen for the session to avoid jumps.
  ritualOrderIds = [...habits.filter((h) => !completions[h.id]), ...habits.filter((h) => completions[h.id])].map((h) => h.id);
  ensureRitualOverlay();
  renderRitual();
}

function closeRitual() {
  const overlay = document.getElementById("ritual");
  if (overlay) overlay.hidden = true;
  document.body.classList.remove("ritual-open");
  ritualOrderIds = null;
}

function renderRitual() {
  const overlay = document.getElementById("ritual");
  if (!overlay) return;

  const today = todayIso();
  const completions = state.completions[today] || {};
  const habits = getHabits();
  const byId = new Map(habits.map((habit) => [habit.id, habit]));
  const orderedAll = (ritualOrderIds || habits.map((habit) => habit.id)).map((id) => byId.get(id)).filter(Boolean);
  habits.forEach((habit) => {
    if (!orderedAll.includes(habit)) orderedAll.push(habit);
  });
  // В ритуал попадает только актуальное сегодня — отдыхающие по ритму не мельтешат.
  const restingCount = orderedAll.filter((habit) => getHabitStatus(habit).state === "resting").length;
  const ordered = orderedAll.filter((habit) => getHabitStatus(habit).state !== "resting");

  const todayCoins = getDayCoins(today);
  const pct = Math.min(100, (todayCoins / DAILY_TARGET) * 100);
  const streak = getStreakDays();
  const doneCount = ordered.filter((habit) => completions[habit.id]).length;
  const allDone = doneCount === ordered.length && ordered.length > 0;
  const targetHit = todayCoins >= DAILY_TARGET;

  const radius = 52;
  const circumference = 2 * Math.PI * radius;
  const offset = circumference * (1 - pct / 100);
  const ringCenter = targetHit
    ? `<tspan class="ritual-ring-done">Цель ✓</tspan>`
    : `${todayCoins}<tspan class="ritual-ring-sub"> / ${DAILY_TARGET}</tspan>`;

  const list = ordered
    .map((habit) => {
      const doneTier = getCompletedTier(habit);
      const tiers = getHabitTiers(habit);
      const hasLevels = tiers.length > 1;
      const done = Boolean(doneTier);
      const currentCoins = done ? doneTier.tier.coins : tiers[0].coins;
      const cadence = getCadence(habit);
      const cadenceTag = cadence !== "daily" ? ` · ${CADENCES[cadence].chip}` : "";
      const sub = (done && hasLevels ? `${sectors[habit.sector]?.label || "Сектор"} · ${escapeHtml(doneTier.tier.label)}` : sectors[habit.sector]?.label || "Сектор") + cadenceTag;
      const hStreak = getHabitStreak(habit);
      const streakChip = hStreak >= 2 ? ` <span class="habit-streak">🔥 ${hStreak}</span>` : "";
      return `
        <button type="button" class="ritual-habit ${done ? "done" : ""}" data-ritual-habit="${habit.id}">
          <span class="ritual-check" aria-hidden="true">${done ? "✓" : "+"}</span>
          <span class="ritual-habit-main">
            <strong><span class="item-emoji">${iconForHabit(habit)}</span>${escapeHtml(habit.title)}${hasLevels ? ' <span class="habit-levels-hint">▾</span>' : ""}${streakChip}</strong>
            <span>${sub}</span>
          </span>
          <span class="ritual-coin">+${currentCoins}</span>
        </button>
      `;
    })
    .join("");

  overlay.innerHTML = `
    <div class="ritual-card">
      <div class="ritual-head">
        <div class="ritual-ring">
          <svg viewBox="0 0 120 120" aria-hidden="true">
            <circle class="ritual-ring-bg" cx="60" cy="60" r="${radius}" />
            <circle class="ritual-ring-fg" cx="60" cy="60" r="${radius}" stroke-dasharray="${circumference.toFixed(1)}" stroke-dashoffset="${offset.toFixed(1)}" />
            <text x="60" y="60" text-anchor="middle" dominant-baseline="central" class="ritual-ring-text">${ringCenter}</text>
          </svg>
        </div>
        <div class="ritual-head-meta">
          <p class="onboarding-eyebrow">Ритуал дня</p>
          <h2 class="ritual-title">${allDone ? "День закрыт. Красиво." : "Отметь, что уже сделал"}</h2>
          <p class="ritual-streak">🔥 ${formatDays(streak)} · рекорд ${formatDays(state.streak?.best || 0)} · ${state.streak?.shields || 0} 🛡️</p>
        </div>
      </div>
      <div class="ritual-list">${list || '<p class="empty-state">На сегодня всё закрыто. Остальные привычки отдыхают по плану.</p>'}</div>
      ${restingCount ? `<p class="ritual-resting-note">😴 ${restingCount} ${habitWord(restingCount)} ${restingCount === 1 ? "отдыхает" : "отдыхают"} по плану — вернутся, когда подойдёт срок.</p>` : ""}
      <div class="ritual-actions">
        <button type="button" class="ritual-done" data-ritual="close">${allDone ? "Готово 🎉" : "Готово"}</button>
      </div>
    </div>
  `;

  overlay.querySelectorAll("[data-ritual-habit]").forEach((button) => {
    button.addEventListener("click", () => {
      const habit = byId.get(button.dataset.ritualHabit);
      if (!habit) return;
      if (getHabitTiers(habit).length > 1) {
        openAmountSheet(habit);
      } else {
        toggleHabit(habit, button);
        renderRitual();
      }
    });
  });
  overlay.querySelector('[data-ritual="close"]').addEventListener("click", () => closeRitual());
}

function registerServiceWorker() {
  if (!("serviceWorker" in navigator)) return;
  if (!window.location.protocol.startsWith("http")) return;
  navigator.serviceWorker.register("sw.js").catch(() => {});
}

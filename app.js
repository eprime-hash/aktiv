function updateFutureInputState() {
  if (!els.fsInput) return;
  const ready = Boolean(supabase && cloudUser);
  els.fsInput.disabled = !ready;
  els.fsSendBtn.disabled = !ready;
  if (!ready) setFsHint("Войди в облако (Настройка → Облако), чтобы говорить с собой будущим живьём.");
  else if (els.fsInputHint.textContent.startsWith("Войди")) setFsHint("");
}

function buildFutureContext() {
  const today = todayIso();
  const recent = [];
  getLastDays(3).forEach((d) => {
    Object.values(state.completions[d] || {}).forEach((e) => {
      const h = getHabitById(e.habitId);
      if (h) recent.push(h.title);
    });
  });
  const level = getLevel();
  return {
    goal: state.profile?.bigGoal || "",
    archetype: archetypes[state.profile?.archetype]?.label || "",
    streak: getStreakDays(),
    best: state.streak?.best || 0,
    level,
    rank: getRank(level),
    todayCoins: getDayCoins(today),
    dailyTarget: DAILY_TARGET,
    recentHabits: [...new Set(recent)].slice(0, 6).join(", "),
    letter: state.profile?.futureLetter || "",
    trust: Math.round(state.bond?.trust || 0),
    bondTier: getBondTier(state.bond?.trust || 0).name,
    debt: state.bond?.debt || 0,
  };
}

async function callFutureSelf(message) {
  const history = (state.futureSelf.messages || [])
    .slice(1, 15)
    .reverse()
    .map((m) => ({ role: m.from === "you" ? "user" : "assistant", content: m.text }));
  const { data, error } = await supabase.functions.invoke("future-self", {
    body: { context: buildFutureContext(), history, message },
  });
  if (error) throw error;
  if (data?.error) throw new Error(data.error);
  return data?.text || "…";
}

async function sendFutureMessage() {
  if (fsSending) return;
  const text = (els.fsInput?.value || "").trim();
  if (!text) return;
  if (!supabase || !cloudUser) {
    setFsHint("Войди в облако (Настройка → Облако), чтобы говорить с собой будущим живьём.");
    return;
  }
  fsSending = true;
  els.fsInput.value = "";
  setFsHint("");
  state.futureSelf.messages.unshift({ id: `fs-${Date.now()}`, date: new Date().toISOString(), type: "chat", text, from: "you" });
  state.futureSelf.messages = state.futureSelf.messages.slice(0, 80);
  saveState();
  fsTyping = true;
  renderFutureFeed();

  try {
    const reply = await callFutureSelf(text);
    state.futureSelf.messages.unshift({ id: `fs-${Date.now() + 1}`, date: new Date().toISOString(), type: "chat", text: reply, from: "future" });
    state.futureSelf.messages = state.futureSelf.messages.slice(0, 80);
    markFutureSeen();
    saveState();
  } catch {
    setFsHint("Не получилось связаться. Проверь, что функция future-self развёрнута и ключ ANTHROPIC_API_KEY задан в Supabase.");
  } finally {
    fsTyping = false;
    fsSending = false;
    renderFutureFeed();
  }
}

function renderFutureSelf() {
  renderFutureFeed();
  updateFutureInputState();
  const totalCoins = getTotalCoins();
  const pace = getProjectedDailyPace();
  const futureCoins = totalCoins + pace * 365;
  const futurePotential = futureCoins * COIN_VALUE;
  const nowPotential = totalCoins * COIN_VALUE;
  const futureLevel = Math.floor(futureCoins / LEVEL_SIZE) + 1;
  const futureStage = getStage(futurePotential).title;
  const identity = archetypes[state.profile?.archetype]?.label || "тот, кем ты решил стать";
  const gap = Math.max(0, futurePotential - nowPotential);

  els.futureMessage.textContent = getFutureMessage(pace);

  els.futureContinuePotential.textContent = money(futurePotential);
  els.futureContinueDetail.textContent = `Уровень ${futureLevel} · ${futureStage}. Ты — ${identity}, на год опытнее и сильнее.`;

  els.futureQuitPotential.textContent = money(nowPotential);
  els.futureQuitDetail.textContent = "Тот же уровень. Навык, форма и энергия без практики тускнеют — актив замирает.";

  els.futureGap.innerHTML = `Цена решения бросить сейчас — <strong>${money(gap)}</strong> непостроенного потенциала за год. Это не деньги в кошельке — это версия тебя, которой не случится.`;

  renderFutureLetter();
}


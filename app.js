function getStakeWeekEnd(weekKey) {
  const monday = new Date(`${weekKey}T00:00:00`);
  monday.setDate(monday.getDate() + 6);
  return formatDate(monday);
}

function isStakeDayMet(date) {
  const protectedSet = new Set(state.streak?.protectedDates || []);
  return Object.keys(state.completions[date] || {}).length >= DAILY_FLOOR_ACTIONS || protectedSet.has(date);
}

function stakePastDays(stake) {
  const days = [];
  const cursor = new Date(`${stake.startDate}T00:00:00`);
  const weekEnd = new Date(`${getStakeWeekEnd(stake.weekKey)}T00:00:00`);
  const yesterday = new Date(`${todayIso()}T00:00:00`);
  yesterday.setDate(yesterday.getDate() - 1);
  const limit = weekEnd < yesterday ? weekEnd : yesterday;
  while (cursor <= limit) {
    days.push(formatDate(cursor));
    cursor.setDate(cursor.getDate() + 1);
  }
  return days;
}

function reconcileStake() {
  const s = state.stake;
  if (!s?.active || s.status === "failed") return;

  const missed = stakePastDays(s).some((date) => !isStakeDayMet(date));
  if (missed) {
    s.status = "failed";
    return;
  }
  s.status = todayIso() > getStakeWeekEnd(s.weekKey) ? "won" : "active";
}


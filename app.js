function wireOnboardingStep() {
  const overlay = document.getElementById("onboarding");
  if (!overlay) return;

  overlay.querySelectorAll("[data-archetype]").forEach((button) => {
    button.addEventListener("click", () => {
      onboardingDraft.archetype = button.dataset.archetype;
      onboardingDraft.habitsInitialized = false;
      renderOnboarding();
    });
  });

  overlay.querySelectorAll("[data-goal]").forEach((button) => {
    button.addEventListener("click", () => {
      onboardingDraft.bigGoal = button.dataset.goal;
      onboardingDraft.goalTarget = Number(button.dataset.target) || 0;
      renderOnboarding();
    });
  });

  overlay.querySelectorAll("[data-habit]").forEach((button) => {
    button.addEventListener("click", () => {
      const id = button.dataset.habit;
      if (onboardingDraft.habitIds.has(id)) onboardingDraft.habitIds.delete(id);
      else onboardingDraft.habitIds.add(id);
      renderOnboarding();
    });
  });

  const goalInput = overlay.querySelector("#onbGoalInput");
  if (goalInput) goalInput.addEventListener("input", () => (onboardingDraft.bigGoal = goalInput.value.trim()));
  const targetInput = overlay.querySelector("#onbTargetInput");
  if (targetInput) targetInput.addEventListener("input", () => (onboardingDraft.goalTarget = Math.max(0, Math.round(Number(targetInput.value || 0)))));

  overlay.querySelectorAll("[data-onb]").forEach((button) => {
    button.addEventListener("click", () => handleOnboardingAction(button.dataset.onb));
  });
}

function handleOnboardingAction(action) {
  if (action === "skip") {
    onboardingDraft.archetype = onboardingDraft.archetype || "balanced";
    finishOnboarding(true);
    return;
  }
  if (action === "back") {
    onboardingDraft.step = Math.max(0, onboardingDraft.step - 1);
    renderOnboarding();
    return;
  }
  if (action === "next") {
    onboardingDraft.step = Math.min(3, onboardingDraft.step + 1);
    renderOnboarding();
    return;
  }
  if (action === "finish") {
    finishOnboarding(false);
    return;
  }
  if (action === "cancel") {
    closeOnboarding();
    return;
  }
  if (action === "save") {
    state.profile.bigGoal = onboardingDraft.bigGoal || "";
    state.profile.goalTargetAssets = onboardingDraft.goalTarget || 0;
    saveState();
    closeOnboarding();
    render();
  }
}

